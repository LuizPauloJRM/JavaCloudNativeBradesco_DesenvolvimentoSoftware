public class OperadoresRelacionais {
    public static void main(String[] args) {
        int x = 7;
        int y = 7;
        int z = 10;

        // Verificando igualdade
        System.out.println(x == y);  // true, pois 7 é igual a 7
        System.out.println(x == z);  // false, pois 7 não é igual a 10

        // Verificando desigualdade
        System.out.println(x != z);  // true, pois 7 é diferente de 10
    }
}
